import pytest
from vowel_counter import count_vowels

@pytest.mark.parametrize("input_str,expected", [
    ("aeiouy", 6),  # только английские гласные
    ("АЕЁИОУЫЭЮЯ", 10),  # только русские гласные
    ("bcdfg", 0),  # только согласные
    ("", 0),  # пустая строка
    ("Hello, World!", 3),  # смешанная строка
    ("Привет, мир!", 3),  # смешанная строка на русском
    ("AaEeIiOoUuYy", 12),  # все английские гласные в разных регистрах
    ("Съешь ещё этих мягких французских булок, да выпей чаю.", 17),  # длинная фраза с разными буквами
    ("1234567890", 0),  # только цифры
    ("!@#$%^&*()", 0),  # только спецсимволы
    ("aAеЕёЁиИoOуУыЫэЭюЮяЯ", 20),  # все гласные в разных регистрах (рус+англ)
])
def test_count_vowels(input_str, expected):
    assert count_vowels(input_str) == expected 